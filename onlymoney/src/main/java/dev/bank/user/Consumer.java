package dev.bank.user;

public class Consumer {
	private int creditRating;
	private int installment;
	
	public Consumer(int creditRating, int installment) {
		super();
		this.creditRating = creditRating;
		this.installment = installment;		// 할부 기간
	}

	public int getCreditRating() {
		return creditRating;
	}

	public int getInstallment() {
		return installment;
	}
	
	
	
	
	//private Stirng[]
}
